public class Drink {

    private double price;
    private String brand;
    

    public void displayBrandAndPrice() {
       
        System.out.println("Brand" + brand + (String.format("%.2f", price)));
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    

}
